# Part 1: Operations with integers
print("Part 1: Integer Operations")
print("-" * 30)

# Get integer inputs from user
num1 = int(input("Enter first integer: "))
num2 = int(input("Enter second integer: "))

# Perform operations with integers
sum_int = num1 + num2
diff_int = num1 - num2
prod_int = num1 * num2
quot_int = num1 / num2

# Display results for integers
print("\nResults for integers:")
print(f"Sum: {num1} + {num2} = {sum_int}")
print(f"Difference: {num1} - {num2} = {diff_int}")
print(f"Product: {num1} * {num2} = {prod_int}")
print(f"Quotient: {num1} รท {num2} = {quot_int}")

