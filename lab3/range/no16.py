# Generate a list of even numbers from 2 to 20
even_numbers = list(range(2, 21, 2))  # Start at 2, go up to (but not including) 21, step by 2
print("Even numbers from 2 to 20:", even_numbers)

# Create a reverse range and convert it into a list
reverse_range = list(range(20, 0, -1))  # Start at 20, go down to (but not including) 0, step by -1
print("Reverse range from 20 to 1:", reverse_range)